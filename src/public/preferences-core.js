export function normalizeLanguage(value, fallback = 'zh') {
  return LANGUAGES.some((item) => item.value === value) ? value : fallback;
}

export function normalizeTheme(value, fallback = 'system') {
  return THEMES.some((item) => item.value === value) ? value : fallback;
}

export function translate(language, key, values = {}) {
  const lang = normalizeLanguage(language, 'en');
  const template = MESSAGES[lang][key] || MESSAGES.en[key] || key;
  return template.replace(/\{(\w+)\}/g, (_match, name) => values[name] ?? '');
}
